class Rectangle
{
	public static void area()
	{
		int len = 10 ;
		int wid = 5 ;
		int area = len * wid ;
		System.out.println("AREA : "+ area );
	}

	public static void perimeter()
	{
		int len = 10 ;
		int wid = 5 ;
		int peri = 2 * (len + wid);
		System.out.println("PERIMETER "+ peri);
	}
	
	public static void main(String[] args)
	{
		area();
		perimeter();
	}
}
		





 






