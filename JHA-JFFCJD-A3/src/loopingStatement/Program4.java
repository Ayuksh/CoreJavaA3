package loopingStatement;

public class Program4 {
    public static void main(String[] args) {
       for (int j =1 ; j<=10 ; j++)
       {
           for (int i =1 ; i<=10 ; i++)
           {
               System.out.println(j+" X "+i +" = "+(j*i));
           }
           System.out.println("\n\n");
       }
    }
}
